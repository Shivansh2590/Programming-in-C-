/*This is the program  of Char datatype demonstration*/
/*Char datatype take input and give output in character point*/

#include<iostream>
using namespace std;

int main()
{
    char a[10];                              // [10] is used to give more character reading capacity
    cout<<"Enter The word :";
    cin>>a;

    cout<<"The word is :"<<a;
    return 0;
}